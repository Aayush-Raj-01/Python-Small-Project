output = []

for line in lines:
    # 1. Remove timestamp + name
    line = re.sub(r'\[\d+\/\d+\/\d+.*?\]\s*\w+:\s*', '', line)

    # 2. Remove long hex strings (continuous like 92734cd7f45bcb15)
    line = re.sub(r'\b[0-9a-fA-F]{8,}\b', '', line)

    # 3. Split into hex pairs only
    pairs = re.findall(r'\b[0-9a-fA-F]{2}\b', line)

    if not pairs:
        continue

    # 4. Convert to \x format
    converted = ''.join([f'\\x{p.lower()}' for p in pairs])

    # 5. Wrap in quotes
    output.append(f'"{converted}",')

# Final output
